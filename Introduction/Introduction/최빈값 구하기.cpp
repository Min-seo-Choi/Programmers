#include <string>
#include <vector>
#include <map>
#include <algorithm>

using namespace std;

// 최빈값은 주어진 값 중에서 가장 자주 나오는 값을 의미합니다. 
// 정수 배열 array가 매개변수로 주어질 때, 최빈값을 return 하도록 solution 함수를 완성해보세요. 
// 최빈값이 여러 개면 -1을 return 합니다.

int solution(vector<int> array)
{
	sort(array.begin(), array.end());

	int num = -1, count = 0;
	map<int, int> result;

	for (int arr : array)
	{
		if (num == -1) num = arr;
		else if (num != arr)
		{
			result.insert({ num, count });

			num = arr;
			count = 0;
		}
		count++;
	}

	result.insert({ num, count });

	count = 0;
	int max = 0;
	bool isSame = false;

	for (auto res : result)
	{
		if (count < res.second)
		{
			max = res.first;
			count = res.second; 
			isSame = false;
		}
		else if (count == res.second) isSame = true;
	}

	return isSame ? -1 : max;
}